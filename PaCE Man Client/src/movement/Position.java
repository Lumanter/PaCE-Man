package movement;

/**
 * Basic position representation
 */
public class Position {
    
    // x position
    public int x;
    
    // y position
    public int y;

    /**
     * Constructor sets the initial values of the position
     * 
     * @param x x position
     * @param y y position
     */
    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
